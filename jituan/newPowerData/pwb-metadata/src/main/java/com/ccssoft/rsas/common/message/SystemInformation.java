package com.ccssoft.rsas.common.message;

public class SystemInformation
{
   private static int InformationNO;
   private static String  Variants;
   private static String  Description;
   
   public static int getInformationNO() {
      return InformationNO;
   }
   
   public static void setInformationNO(int InfoNOValue) {
      InformationNO = InfoNOValue;
   }
   
   public static String getVariants() {
      return Variants;
   }
   
   public static void setVariants(String VariantValue) {
      Variants = VariantValue;
   }
   
   public static String getDescription() {
      return Description;
   }
   
   public static void setDescription(String DescriptionValue) {
      Description = DescriptionValue;
   }
}
